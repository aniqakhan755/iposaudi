    <h3 class="content-heading">

        Pre-IPO Considerations


    </h3>
    <p>

        Potential investors always look at various aspects of the company before investing which is a major stepping
        stone to meet the company’s funding requirements in turn. These range from financial position to management
        caliber, which needs to be articulated well to present a compelling investment case. A company needs to consider
        myriad factors and prepare accordingly.


    </p>
    <ul class="mb-20 square-ul">

        <li>Market standing of company’s products and services:</li>
        <li>Experienced management:</li>
        <li>Marketing plan for IPO:</li>
        <li>Right time to go public:</li>
        <li>Impact of dilution of control of key shareholder(s):</li>
        <li>Impact on company’s structure, key contracts, and relationships after IPO:</li>
    </ul>


    <h3 class="content-heading">

        Pre-IPO Activities


    </h3>
    <p>
        For a successful outcome, IPO actionable items should always be reality based and initial planning and
        preparation are a critical success factor for a smooth execution process. Undertaking assessment before an IPO
        helps the company identify gaps between market expectations and its current reality. Each market is unique and
        therefore expectations are different from exchange to exchange and industry to industry. Hence, expert advisors
        who have worked across the globe know the right approach for companies and could devise bespoke service/modules
        based on its specific needs. However, there are a few key areas that must be universally addressed by each
        company.


    </p>
    <ul class="mb-20 square-ul">

        <li>Equity story to improve organizational value</li>
        <li>Specification on usage of proceeds
        </li>
        <li>Optimization and preparation of policies and procedures</li>
        <li>Execution of corporate governance structure</li>
        <li>Due diligence of legal aspects</li>
        <li> Appointment and engagement with lead bankers and other partners</li>


    </ul>
    <h3 class="content-heading">

        Bridging the Gaps


    </h3>
    <p>Pre-assessment will help in getting a clear route for the company to meet the market expectations. Therefore, the
        next step is to translate the requirements into achievable plans. The company must develop a carefully crafted
        detailed action plan to achieve those goals. Sometimes, company planners set an unreasonable target for their
        staff that leads to a higher attrition rate during a crucial IPO process. Therefore, IPO candidates should work
        with an experienced advisor who will identify the most important issues that need to be resolved and are
        feasible to be completed before the IPO.</p>
    <h3 class="content-heading">

        Disclosures in prospectus include


    </h3>
    <ul class="mb-20 square-ul">
        <li> Uncertainties related to business</li>
        <li> Information on how IPO proceeds would be utilized</li>
        <li> Company’s current and potential dividend policy, along with limitations</li>
        <li> Its capital structure pre- and post-listing</li>
        <li> Company’s business model, including revenue and cost drivers</li>
        <li> Audited financial statements for 3–5 years, along with interim statement</li>
        <li> Pro forma financial information including important and recent transactions or events, such as convertible
            debentures, that could impact number of shares on closing date of IPO
        </li>
        <li> Management discussion and analysis</li>
    </ul>
    <h3 class="content-heading">

        Due diligence procedures


    </h3>
    <ul class="mb-20 square-ul">
        <li> Conducted by the underwriters’ counsel and includes a comprehensive analysis of the company and its
            management
        </li>
        <li>Ensures the prospectus contains no misleading statement, omission, or inconsistency of any information.</li>

    </ul>
    <h3 class="content-heading">


        Preliminary prospectus


    </h3>
    <ul class="mb-20 square-ul">
        <li> Prepared by the lead underwriter to the interested investors to form an underwriting syndicate in order to
            apportion the shares.
        </li>


    </ul>
